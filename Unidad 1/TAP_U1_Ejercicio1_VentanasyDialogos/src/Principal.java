    private void jButton2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jButton2KeyPressed
        /*int codigoASCII = evt.getKeyCode();
        
        if(codigoASCII == 64){
            //arroba
        }
        
        if(codigoASCII == 92){
            //diagonal invertida
        }*/
        
        char letra = evt.getKeyChar();
        
        if(letra == 'a' || letra == 'A'){
            int respuesta = JOptionPane.showConfirmDialog(this, "SEGURO QUE DESEAS SALIR?", "ATENCION!!", YES_NO_OPTION, ERROR_MESSAGE);
            
            if (respuesta == YES_OPTION) {
                setTitle("ELEGISTE S√ç :)");
            }
            
            if (respuesta == NO_OPTION) {
                setTitle("elegiste no:c");
            }
        }
        
        if(letra == 'b' || letra == 'B'){
            String nombre = JOptionPane.showInputDialog("ESCRIBA SU NOMBRE");
            setTitle("SU NOMBRE ES: "+nombre);
        }
        
        if(letra == 't' || letra == 'T'){
            JOptionPane.showMessageDialog(this, "ESTA ES UNA ADVERTENCIA", "IMPORTANTE DE LEER", JOptionPane.WARNING_MESSAGE);
            
        }
        
        if(letra == 'i' || letra == 'I'){
            JOptionPane.showInternalInputDialog(jDesktopPane1,"HOLIS");
        }
        
        
    }//GEN-LAST:event_jButton2KeyPressed

